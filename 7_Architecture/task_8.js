// Создание простого HTTP-сервера в Node.js

// Подключаем модуль http
const http = require('http');

// Создаем сервер и определяем обработчик запросов
const server = http.createServer((req, res) => {
    // Логируем каждый входящий запрос
    console.log('request event');
    // Отправляем ответ клиенту
    res.end('Hello World');
});

// Запускаем сервер на порту 3000
server.listen(3000, () => {
    console.log('Сервер запущен: http://localhost:3000');
});

/*
Разберем этот код подробнее:

Создание сервера:
http.createServer() создает новый HTTP-сервер
Принимает функцию-обработчик с параметрами:
req (request) - объект с информацией о входящем запросе
res (response) - объект для формирования ответа
Обработка запросов:
При каждом запросе к серверу:
Выводится сообщение "request event"
Отправляется ответ "Hello World"
Запуск сервера:
server.listen(3000) запускает сервер на порту 3000
После запуска выводится сообщение о том, что сервер слушает порт
Как проверить работу:
Запустить файл через Node.js
Открыть браузер и перейти по адресу http://localhost:3000
В браузере отобразится "Hello World"
В консоли будет видно логи запросов
Это базовый пример создания веб-сервера, который демонстрирует:

Асинхронную обработку запросов
Работу с HTTP-протоколом
Обработку событий в Node.js
*/