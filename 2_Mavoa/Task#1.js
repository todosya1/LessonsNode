/*
Условия задачи
Дано число (например, const num = 123).
Программа должна проверять это число на определенные условия и выполнять действия:
Если число делится на 1, 2 или 3, вывести 1 (работаем с числом как единым целым).
Если число делится на 2 или 3, но не на 1, вывести 2 (работаем с двумя частями числа).
Если число делится на 3, но не на 2 и 1, вывести 3 (работаем с тремя частями числа).
Ограничения
Использование if-else запрещено.
Нужно максимально использовать логику JavaScript без явных условий.
*/

const num = 150;
console.log("asdasd")

// Проверяем, что num является числом
if (!Number.isInteger(num)) {
  console.log(false);
} else {
  // Используем массив с результатами для упрощения логики
  const result = [
    num % 1 === 0 && num % 2 !== 0 && num % 3 !== 0 ? 1 : 0,
    num % 2 === 0 && num % 3 !== 0 ? 2 : 0,
    num % 3 === 0 && num % 2 !== 0 && num % 1 !== 0 ? 3 : 0
  ].find(value => value !== 0) || 0;

  console.log(result);
}
