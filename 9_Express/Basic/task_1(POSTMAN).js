const express = require('express');  // Импортируем Express
const app = express();               // Создаем приложение Express

// Устанавливаем порт, используя переменные окружения или значение по умолчанию
const port = process.env.PORT || 3000;

// Определяем маршрут для корневого URL
app.get('/', (req, res) => {
    res.send('Hello, World!');       // Отправляем ответ
});

app.get('/products', (req, res) => {
    res.send("All products");
});

// Запускаем сервер на заданном порту
app.listen(port, () => {
    console.log(`Сервер запущен на порту http://localhost:${port}`);
});

/*
Объяснение:
1. Импорт и инициализация:
   - express импортируется и используется для создания приложения `app`.
2. Порт:
   - Порт задается через переменные окружения или по умолчанию 3000.
   - Это позволяет запускать приложение на разных машинах или в облаке.
3. Маршрут:
   - `app.get('/')` определяет обработчик для GET-запросов на корневой URL.
   - Отправляет "Hello, World!" в ответ на запрос.
4. Запуск сервера:
   - `app.listen(port)` запускает сервер и начинает слушать заданный порт.
5. Как использовать:
   - Убедитесь, что Express установлен (`npm install express`).
   - Запустите сервер с помощью `node <filename>.js`.
   - Перейдите по адресу `http://localhost:3000` в браузере, чтобы увидеть "Hello, World!".
*/

