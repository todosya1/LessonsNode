// Middleware функция для логирования запросов
// req - объект запроса (содержит информацию о входящем запросе)
// res - объект ответа (используется для отправки ответа клиенту)
// next - функция для передачи управления следующему middleware
const logger = (req, res, next) => {
    // Получаем текущее время в ISO формате и информацию о запросе
    // new Date() - создает объект текущей даты и времени
    // toISOString() - преобразует дату в строку формата ISO
    // req.method - метод запроса (GET, POST, etc.)
    // req.url - путь запроса
    console.log(`[${new Date().toISOString()}] ${req.method} ${req.url}`);
    
    // Передаем управление следующему middleware
    next();
};

// Экспортируем функцию logger, чтобы использовать в других файлах
// через require('./logger')
module.exports = logger;
