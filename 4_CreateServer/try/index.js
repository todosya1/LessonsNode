// 1. Импортируем модуль http
const http = require('http');

// 2. Создаём HTTP-сервер
const server = http.createServer((req, res) => {
    // Отправляем ответ клиенту
    res.end('Hello, Node.js server!');
});

// 3. Сервер начинает прослушивать порт 3000
const PORT = 3000;
server.listen(PORT, () => {
    console.log(`The server is running on http://localhost:${PORT}`);
});

/*
Объяснение кода:
http.createServer:

Создаёт HTTP-сервер.
Функция принимает два аргумента:
req (запрос): объект, содержащий данные о запросе.
res (ответ): объект, используемый для отправки ответа клиенту.
res.end:

Завершает ответ и отправляет данные клиенту.
server.listen:

Запускает сервер, который начинает слушать запросы на указанном порту.
Проверка изменений:
После внесения изменений в код сервера вам нужно остановить сервер (Ctrl + C в терминале) и перезапустить его, выполнив node index.js.
*/